// Section icon component that returns the appropriate icon based on section type
export function SectionIcon({ 
  section, 
  className = '', 
  size = 24 
}: { 
  section: 'cash' | 'bank' | 'mobile' | 'lend'; 
  className?: string; 
  size?: number;
}) {
  switch (section) {
    case 'cash':
      return <CashIcon className={className} size={size} />;
    case 'bank':
      return <BankIcon className={className} size={size} />;
    case 'mobile':
      return <MobileIcon className={className} size={size} />;
    case 'lend':
      return <LendIcon className={className} size={size} />;
    default:
      return <WalletIcon className={className} size={size} />;
  }
}

